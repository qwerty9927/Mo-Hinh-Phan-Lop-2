    private void tblcustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblcustomerMouseClicked
        int i = tblcustomer.getSelectedRow();
        int column = 1;
        if (i >= 0) {
            txtFullName.setText(tblcustomer.getModel().getValueAt(i, column++).toString());
            txtPassword.setText(tblcustomer.getModel().getValueAt(i, column++).toString());
            txtAddress.setText(tblcustomer.getModel().getValueAt(i, column++).toString());
            txtCity.setText(tblcustomer.getModel().getValueAt(i, column++).toString());
        } 
    }//GEN-LAST:event_tblcustomerMouseClicked

    
