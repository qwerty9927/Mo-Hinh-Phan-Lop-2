    private void nuAadd2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nuAadd2ActionPerformed
        Customers c = new Customers();
        c.setFullname(txtFullName.getText());
        c.setPassword(txtPassword.getText());
        c.setAddress(txtAddress.getText());
        c.setCity(txtCity.getText());
        cBLL.addCustomer(c);
        JOptionPane.showMessageDialog(null, "Thêm thành công");
        loadcustomer();
    }//GEN-LAST:event_nuAadd2ActionPerformed

    private void nutDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nutDeleteActionPerformed
        int i = tblcustomer.getSelectedRow();
        int column = 0;
        if (i >= 0) {
            int id = Integer.parseInt(tblcustomer.getModel().getValueAt(i, column++).toString());
            cBLL.deleteCustomer(id);
            JOptionPane.showMessageDialog(null, "Xóa thành công");
            loadcustomer();
        }
    }//GEN-LAST:event_nutDeleteActionPerformed

    private void nutEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nutEditActionPerformed
        int i = tblcustomer.getSelectedRow();
        if (i >= 0) {
            int id = Integer.parseInt(tblcustomer.getModel().getValueAt(i, 0).toString());
            Customers c = new Customers();
            c = cBLL.getCustomer(id);
           
            c.setFullname(txtFullName.getText().toString());
            c.setPassword(txtPassword.getText().toString());
            c.setAddress(txtAddress.getText().toString());
            c.setCity(txtCity.getText().toString());
            cBLL.updateCustomer(c);
            JOptionPane.showMessageDialog(null, "Sửa thành công");
            loadcustomer();
        } 
    }//GEN-LAST:event_nutEditActionPerformed

