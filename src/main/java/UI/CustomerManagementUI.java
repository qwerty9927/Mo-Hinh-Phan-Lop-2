    private void nutsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nutsearchActionPerformed
        String name = search.getText();
        List<Customers> listFound = cBLL.searchCustomer(name);
        if (listFound.size() > 0) {
            Object[][] data = cBLL.convertList(listFound);
            String[] titles = {"Id", "Fullname", "Password", "Address", "City"};
            DefaultTableModel customerModel = new DefaultTableModel(data, titles);
            tblcustomer.setModel(customerModel);
            JOptionPane.showMessageDialog(null, "Tìm thấy");
        } else {
            JOptionPane.showMessageDialog(null, "Không tìm được kết quả");
        }
    }//GEN-LAST:event_nutsearchActionPerformed

